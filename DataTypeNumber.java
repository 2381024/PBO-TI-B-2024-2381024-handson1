public class DataTypeNumber {
    public static void main(String[] args) {
        byte byte_data = 10;
        short short_data = 10;
        int int_data = 10;
        long long_data = 10L;

        //Bilangan Pecahan
        float float_data = 19.7F;
        double double_data = 24.1;

        //Sistem Bilangan
        int decimal_int = 15;
        int hex_decimal = 0xFFA92;
        int bin_decimal = 0b0101;

        int bil = 100_000_000;
    }
}
